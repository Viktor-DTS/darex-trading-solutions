/**
 * Перевірка: рядки «Итог» посеред файлу не обривають розбір наступних рухів.
 * Запуск: node lib/vedomostImport.subtotal.test.js
 */
const assert = require('assert');
const { parseVedomostRows } = require('./vedomostImport');

function buildSyntheticRows() {
  const header = [
    'Дата оплаты', 'Контрагент', 'Ответственный', 'Подразделение', 'Проведен', 'Склад',
    'Документ движения (регистратор)', 'Базовая единица измерения',
    'Склад отправитель', 'Склад получатель', 'Комментарий', 'Ответственный менеджер',
    'Сумма документа', 'Валюта документа',
    'Начальный остаток', 'Приход', 'Расход', 'Конечный остаток',
  ];
  const rows = [
    ['Период: 01.04.2026 - 20.07.2026'],
    [],
    header,
    ['', '', '', '', '', '', 'СКЛАД А', '', '', '', '', '', '', '', '', '', '', ''],
    ['', '', '', '', '', '', 'Товар А', 'шт', '', '', '', '', '', '', 0, 0, 0, 5],
    ['', '', '', '', '', '', 'Перемещение товаров А0000000001 от 10.06.2026 10:00:00', '', '', '', '', '', '', '', 0, 1, 0, 4],
    ['', '', '', '', '', '', '', '', 'СКЛАД А', 'СКЛАД Б', '', '', '', '', 0, 1, 0, 4],
    ['Итог', '', '', '', '', '', '', '', '', '', '', '', '', '', 0, 1, 0, 4],
    ['', '', '', '', '', '', 'СКЛАД Б', '', '', '', '', '', '', '', '', '', '', ''],
    ['', '', '', '', '', '', 'Товар Б', 'шт', '', '', '', '', '', '', 0, 0, 0, 3],
    ['', '', '', '', '', '', 'Перемещение товаров А0000000999 от 15.07.2026 12:00:00', '', '', '', '', '', '', '', 0, 1, 0, 2],
    ['', '', '', '', '', '', '', '', 'СКЛАД Б', 'СКЛАД А', '', '', '', '', 0, 1, 0, 2],
  ];
  return rows;
}

function run() {
  const parsed = parseVedomostRows(buildSyntheticRows());
  assert.strictEqual(parsed.parseStats.subtotalRowsSkipped, 1, 'один рядок Итог пропущено');
  const moves = parsed.movements.filter((m) => m.docType === 'move');
  assert.ok(moves.length >= 2, `очікуємо ≥2 переміщення, отримано ${moves.length}`);
  const max = parsed.parseStats.maxDocDateByType.move;
  assert.ok(max && max.getMonth() === 6 && max.getDate() === 15, 'max move date має бути 15.07.2026');
  console.log('OK: subtotal rows do not truncate parsing');
  console.log('  moves:', moves.length, 'maxDocDate move:', max.toISOString().slice(0, 10));
}

run();
