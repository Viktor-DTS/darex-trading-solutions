function hashStr(s) {
  let h = 2166136261;
  const str = String(s || '');
  for (let i = 0; i < str.length; i++) {
    h ^= str.charCodeAt(i);
    h = Math.imul(h, 16777619);
  }
  return Math.abs(h >>> 0);
}

function curatedJokesEnabled() {
  const raw = process.env.ASSISTANT_CHAT_CURATED_JOKES;
  if (raw === undefined || raw === null || String(raw).trim() === '') return true;
  const v = String(raw).trim().toLowerCase();
  return v !== '0' && v !== 'false' && v !== 'no' && v !== 'off';
}

/**
 * @param {{ login?: string, conversationId?: string, userMsg?: string, lastAssistantContent?: string }} ctx
 */
function pickCuratedJoke(ctx = {}) {
  const pool = [...CURATED_JOKES_UK];
  const last = String(ctx.lastAssistantContent || '').trim();
  if (last) {
    const idx = pool.findIndex((j) => j.trim() === last);
    if (idx >= 0) pool.splice(idx, 1);
  }
  if (!pool.length) return CURATED_JOKES_UK[0];

  const day = Math.floor(Date.now() / 86400000);
  const seed = hashStr(
    `${ctx.login || ''}:${ctx.conversationId || ''}:${day}:${String(ctx.userMsg || '').length}:${pool.length}`,
  );
  return pool[seed % pool.length];
}

