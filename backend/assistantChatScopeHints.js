/**
 * Чат DTS не створює файли звітів; користувач інколи переходить на такі запити після списку заявок.
 */

/**
 * @param {string} messageText
 */
function userMessageAsksOutOfChatScope(messageText) {
  const s = String(messageText || '').toLowerCase().replace(/\s+/g, ' ');
  if (!s.trim()) return false;
  const reportOrFile =
    /звіт|звіти|звіту|експорт|експортуй|експортован|excel|pdf|xlsx|\bxls\b|табличн\w*\s+(у\s+|в\s+|до\s+)файл|файлов\w+|до\s+друку|роздрук|друкувати|скача|скачува|завантаж(ити)?\s+(звіт|файл|таблиц)|згенер(уй|увати)\s+(звіт|файл|документ|таблиц)|підготуй\s+(звіт|файл)|підготувати\s+(звіт|файл)|виписк\w+|довідку\s+(у\s+|в\s+)?файл|аналітичн\w+|зведення.*файл|зведені\s+дані\s+(.{0,20})?(файл|excel)|по\s+(цьом(у|и)|дан(ому|ій))\s+контрагент/.test(
      s,
    );
  const formVerb = /сформуй|сформувати|створ(и|ити)\s+(звіт|файл)|зібра(ти)?\s+звіт|підстав(ь|ити)\s+(в\s+|у\s+)звіт|зроби\s+(мені\s+)?(звіт|експорт)/.test(s);
  return reportOrFile || formVerb;
}

/**
 * Додає блок-інструкцію перед викликом LLM (українською для моделі та користувача в відповіді).
 *
 * @param {string} userMsg
 * @param {string} contentForChat
 * @param {number} discoveryOpenActionsCount
 */
function appendAssistantScopeHintToUserPayload(userMsg, contentForChat, discoveryOpenActionsCount) {
  if (!userMessageAsksOutOfChatScope(userMsg)) return contentForChat;
  const hasDiscovery = contentForChat.includes('[DTS-discovery]');

  const btnsNote =
    discoveryOpenActionsCount > 1
      ? ` Є кнопки «Відкрити форму» під чатом для ${discoveryOpenActionsCount} заявок — згадай про них. `
      : discoveryOpenActionsCount === 1
        ? ` Можна відкрити картку заявки через пошук/таблицю в DTS або відповідну кнопку. `
        : ` `;

  const noRepeatNote = hasDiscovery
    ? `\nНе дублюй довгі переліки заявок, які вже були вище в діалозі, якщо користувач лише попросив «звіт» — нагади коротко (напр. «ті самі три заявки») або вкажи кнопки.\n`
    : '\n';

  return (
    `${contentForChat}\n\n` +
    `[DTS-assistant-scope] Користувач просить дію поза межами текстового асистента: згенерувати/прикріпити **файл звіту**, Excel/PDF, масово «вигрузити» дані або інше виконання, якого в чаті немає. Не створюй вигаданих файлів і не обіцяй їх надсилання з чату.` +
      `\nВідповідай Українською у такій логіці:\n` +
      `1) Одне-два речення: якщо звучить як «Я зараз обмежений функціями в цьому чаті: тут немає згенерованого звіту чи вкладення файлу» — цього достатньо за змістом.\n` +
      `2) Нумерований короткий перелік доступних альтернатив **для роботи з уже знайденими заявками** (за потреби згодуй блоки [DTS], [DTS-discovery], [DTS-discovery-access], [DTS/User]):` +
      `\n   1) Текстовий підсумок або порівняння полів із даних блоків без вигадок.` +
      `\n   2) Пояснення облікових обмежень, якщо це доречно до запиту.` +
      `\n   3) ${hasDiscovery ? 'Нагадай про можливість відкрити картку через кнопки під чатом для кожного номера або через пошук у DTS.' : 'Підказка відкрити картку заявки в DTS (глобальний пошук, таблиця).'}` +
      `\n   4) Якщо потрібен саме файловий звіт: напряму скажи, що він робиться в інтерфейсі DTS — на таблиці заявок (наприклад «Сервісна служба») після фільтра чи пошуку є дія на кшталт «Експорт у Excel»; чат це замінити не може.` +
      `\n   5) За блоком [DTS/UI] — коротко куди в інтерфейсі або яку вкладку відкрити.\n` +
      `3) Завершення: «Оберіть, будь ласка, що з переліченого потрібно» (одним реченням).` +
      `${btnsNote}` +
      `${noRepeatNote}`
  );
}

module.exports = {
  userMessageAsksOutOfChatScope,
  appendAssistantScopeHintToUserPayload,
};
