function chatModel() {
  return String(
    process.env.ASSISTANT_CHAT_MODEL ||
      process.env.PRODUCT_ASSISTANT_LLM_MODEL ||
      'gpt-4o-mini',
  ).trim();
}

function chatBaseUrl() {
  return String(process.env.ASSISTANT_CHAT_BASE_URL || process.env.PRODUCT_ASSISTANT_LLM_BASE_URL || DEFAULT_BASE).replace(
    /\/$/,
    '',
  );
}

function chatTimeoutMs() {
  const t = parseInt(String(process.env.ASSISTANT_CHAT_TIMEOUT_MS || '60000'), 10);
  return Math.min(120000, Math.max(10000, t || 60000));
}

function chatMaxTokens(casual) {
  if (casual) {
    const c = parseInt(String(process.env.ASSISTANT_CHAT_CASUAL_MAX_TOKENS || '520'), 10);
    return Math.min(800, Math.max(200, c || 520));
  }
  const raw = parseInt(String(process.env.ASSISTANT_CHAT_MAX_TOKENS || '1200'), 10);
  const n = raw || 1200;
  return Math.min(8192, Math.max(256, n));
}

function chatFrequencyPenalty() {
  const raw = parseFloat(String(process.env.ASSISTANT_CHAT_FREQUENCY_PENALTY ?? '0.45'));
  if (Number.isNaN(raw)) return 0.45;
  return Math.min(2, Math.max(0, raw));
}

function chatPresencePenalty() {
  const raw = parseFloat(String(process.env.ASSISTANT_CHAT_PRESENCE_PENALTY ?? '0.2'));
  if (Number.isNaN(raw)) return 0.2;
  return Math.min(2, Math.max(0, raw));
}

function chatTemperature(casual) {
  const raw = parseFloat(String(process.env.ASSISTANT_CHAT_TEMPERATURE ?? '0.4'));
  const base = Number.isNaN(raw) ? 0.4 : Math.min(1, Math.max(0, raw));
  if (!casual) return base;
  const bump = parseFloat(String(process.env.ASSISTANT_CHAT_CASUAL_TEMPERATURE ?? '0.72'));
  return Math.min(1, Math.max(base, Number.isNaN(bump) ? 0.72 : bump));
}

/**
 * @param {{ role: string, content: string }[]} messages — без system; він додається тут.
 * @param {{ casual?: boolean }} [options]
 * @returns {{ content: string, model: string }}
 */
async function assistantChatCompletion(messages, options = {}) {
  const casual = Boolean(options.casual);
  const apiKey = resolveLlmApiKey();
  if (!apiKey) {
    const err = new Error('LLM ключ не налаштований (PRODUCT_ASSISTANT_LLM_API_KEY або OPENAI_API_KEY)');
    err.code = 'NO_LLM_KEY';
    throw err;
  }

  const model = chatModel();
  const base = chatBaseUrl();
  const url = `${base}/chat/completions`;

  const body = {
    model,
    temperature: chatTemperature(casual),
    max_tokens: chatMaxTokens(casual),
    frequency_penalty: chatFrequencyPenalty(),
    presence_penalty: chatPresencePenalty(),
    messages: [{ role: 'system', content: ASSISTANT_SYSTEM }, ...messages],
  };

  const res = await fetch(url, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      Authorization: `Bearer ${apiKey}`,
    },
    body: JSON.stringify(body),
    signal: AbortSignal.timeout(chatTimeoutMs()),
  });

  if (!res.ok) {
    const errText = await res.text().catch(() => '');
    const err = new Error(`LLM HTTP ${res.status}: ${errText.slice(0, 200)}`);
    err.code = 'LLM_HTTP_ERROR';
    err.status = res.status;
    throw err;
  }

  const data = await res.json();
  const rawContent = data?.choices?.[0]?.message?.content;
  if (!rawContent || typeof rawContent !== 'string') {
    const err = new Error('Модель не повернула текст відповіді');
    err.code = 'LLM_EMPTY';
    throw err;
  }

  const content = sanitizeAssistantReply(String(rawContent).trim(), { casual });

  return { content, model };
}

