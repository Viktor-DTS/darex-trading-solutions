/**
 * Render static site build: inject API URL + optional secret into dashboard/config.js
 */
const fs = require('fs');
const path = require('path');

const rawUrl = String(process.env.FX_PUBLIC_API_URL || '').trim();

let apiBase = rawUrl.replace(/\/+$/, '');
if (apiBase && !/^https?:\/\//i.test(apiBase)) {
  const host = apiBase.replace(/^https?:\/\//i, '');
  if (!host.includes('.')) {
    apiBase = `https://${host}.onrender.com`;
  } else {
    apiBase = `https://${host}`;
  }
}

const outPath = path.join(__dirname, '../dashboard/config.js');
const content = `// Generated by scripts/build-panel-config.js — do not edit on Render
window.FX_API_BASE = ${JSON.stringify(apiBase)};
`;

fs.writeFileSync(outPath, content, 'utf8');
console.log('[build-panel-config] FX_API_BASE =', apiBase || '(same-origin)');
